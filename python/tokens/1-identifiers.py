# An identifier is a name given to entities like variables, functions, classes, modules, and other objects in Python. It is used to uniquely identify them.


# The rules for naming identifiers in Python are:

# • Identifiers can contain letters (uppercase and lowercase)[a-z,A-Z], digits[0-9], and underscores (_).
# • The name must start with a letter or an underscore.
# • Identifiers cannot start with a digit.
# • We cannot use special symbols like !, @, #, $, and so on.
# • It should not contain white space.
    # first name, first_name, firstName
# • Identifiers are case-sensitive.
    # firstname, FirstName, FIRSTNAME, 
# • Keywords cannot be used as identifiers.


# Valid identifiers:
# -----------------
# • myVariable
# • _myVariable
# • variable1
# • my_variable


# Invalid identifiers:
# -------------------
# • 1variable (starts with a digit)
# • my-variable (contains a hyphen)
# • my variable (contains a space)
# • def (reserved keyword)